
# Examples of Arithmetic Operator
a = 9
b = 4
 
# Addition of numbers
add = a + b
print(add)

# Subtraction of numbers
sub = a - b
print(sub)

# Multiplication of number
mul = a * b
print(mul)

# Division(float) of number
div_normal_float = a / b
print(div_normal_float)

# Division(floor) of number
div_integer= a // b
print(div_integer)

# Modulo of both number
mod = a % b
print(mod)

# Power
pow = a ** b
print(pow)

# Comparison Operators/Relational Operators
a = 13
b = 33
 
# a > b is False
print(a > b)
 
# a < b is True
print(a < b)
 
# a == b is False
print(a == b)
 
# a != b is True
print(a != b)
 
# a >= b is False
print(a >= b)
 
# a <= b is True
print(a <= b)


# Examples of Logical Operator
a = True
b = False
 
# Print a and b is False
print(a and b)
 
# Print a or b is True
print(a or b)
 
# Print not a is False
print(not a)

