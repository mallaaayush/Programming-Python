print('2+3 =', 2+3) #addition
print('2-3 =', 2-3) #substraction
print('2*3 =', 2*3) #multiplication
print('2/3 =', 2/3) # normal division
print('2//3 =', 2//3) # integer division
print('2%3 =', 2%3)  # remainder division
print("2*5-4*3+6-9//3+8/3+5%3 =", int(2*5-4*3+6-9//3+8/3+5%3)) # int() make it integer from float i.e. 5.666666666666666 into 5